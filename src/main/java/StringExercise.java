public class StringExercise {
    public static void main(String[] args) {
        String noEducation = "We don't need no education" + "\n" + "We don't need no thought control";
        String sInside = "Check \"this\" out!, \"s inside of \"s!";
        String windowsDrive = "In windows, the main drive is usually C:\\";
        String backslashes = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";

        System.out.println(noEducation);
        System.out.println();
        System.out.println(sInside);
        System.out.println();
        System.out.println(windowsDrive);
        System.out.println();
        System.out.println(backslashes);
        System.out.println();

    }
}
